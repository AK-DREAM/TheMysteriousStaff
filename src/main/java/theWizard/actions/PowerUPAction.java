public class PowerUPAction extends AbstractGameAction {
    private boolean freeToPlayOnce = false;
    private boolean upgraded;
    private AbstractPlayer p;
    private int energyOnUse;

    public PowerUPAction(AbstractPlayer p, boolean freeToPlayOnce, int energyOnUse, boolean upgraded) {
        this.p = p;
        this.freeToPlayOnce = freeToPlayOnce;
        this.duration = Settings.ACTION_DUR_XFAST;
        this.actionType = ActionType.SPECIAL;
        this.energyOnUse = energyOnUse;
        this.upgraded = upgraded;
    }

    public void update() {
        int effect = EnergyPanel.totalCount;
        if (this.energyOnUse != -1) {
            effect = this.energyOnUse;
        }
        if (this.p.hasRelic("Chemical X")) {
            effect += 2;
            this.p.getRelic("Chemical X").flash();
        }

        if (this.upgraded) effect += 2;
        else effect++;

        if (!CheckManaCostAction.CheckManaCost(effect)) {
            effect = ExhaustAllManaAction.ExhaustAllManaAction(99);
        }

        if (effect > 0) {
            this.addToBot(new ApplyPowerAction(this.p, this.p, new StrengthPower(this.p, effect), effect));
            this.addToBot(new ApplyPowerAction(this.p, this.p, new DexterityPower(this.p, effect), effect));
            if (!this.freeToPlayOnce) {
                this.p.energy.use(EnergyPanel.totalCount);
            }
        }

        this.isDone = true;
    }
}
